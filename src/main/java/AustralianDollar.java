public class AustralianDollar {

    public double australianDollarToCanadianDollar(double input){
        double finalConversion = input * 1.02;
        return finalConversion;
    }

    public double australianDollarToSingaporeDollar(double input){
        double finalConversion = input * 1.05;
        return finalConversion;
    }

    public double australianDollarToSwissFranc(double input){
        double finalConversion = input * 1.33;
        return finalConversion;
    }
}
