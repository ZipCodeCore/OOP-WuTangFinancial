public class IndianRupee {

    public double indianRupeeToAustralianDollars(double input){
        double finalConversion = input * 50.60;
        return finalConversion;
    }

    public double indianRupeeToCanadianDollar(double input){
        double finalConversion = input * 51.75;
        return finalConversion;
    }

    public double indianRupeeToSinaporeDollar(double input){
        double finalConversion = input * 47.77;
        return finalConversion;
    }

    public double indianRupeeToSwissFranc(double input){
        double finalConversion = input * 67.70;
        return finalConversion;
    }

    public double indianRupeeToMalaysianRinggit(double input){
        double finalConversion = input * 15.28;
        return finalConversion;
    }


    public double indianRupeeToJapaneseYen(double input){
        double finalConversion = input * 1.69;
        return finalConversion;
    }

    public double indianRupeeToChineseYuanRenminbi(double input){
        double finalConversion = input * 9.87;
        return finalConversion;
    }

    public double indianRupeeToAmericanDollar(double input){
        double finalConversion = input * 68.32;
        return finalConversion;
    }
}
